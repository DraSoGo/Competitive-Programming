#include <bits/stdc++.h>
using namespace std;
int main()
{
    int x;
    cin >> x;
    if (x == 1)
    {
        cout << "Sunday";
    }
    else if (x == 2)
    {
        cout << "Monday";
    }
    else if (x == 3)
    {
        cout << "Tuesday";
    }
    else if (x == 4)
    {
        cout << "Wednesday";
    }
    else if (x == 5)
    {
        cout << "Thursday";
    }
    else if (x == 6)
    {
        cout << "Friday";
    }
    else if (x == 7)
    {
        cout << "Saturday";
    }
    else
    {
        cout << "Invalid number.";
    }
    
    
}